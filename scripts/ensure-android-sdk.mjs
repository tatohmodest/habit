/**
 * Writes android/local.properties with sdk.dir if the file is missing or has no sdk.dir.
 * Resolves SDK from: ANDROID_HOME, ANDROID_SDK_ROOT, .env.local, then common install paths.
 */
import { existsSync } from "node:fs";
import { readFile, writeFile } from "node:fs/promises";
import os from "node:os";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, "..");
const androidDir = path.join(root, "android");
const localProps = path.join(androidDir, "local.properties");

async function mergeEnvLocal() {
  const p = path.join(root, ".env.local");
  try {
    const text = await readFile(p, "utf8");
    for (const line of text.split(/\n/)) {
      const t = line.trim();
      if (!t || t.startsWith("#")) continue;
      const eq = t.indexOf("=");
      if (eq === -1) continue;
      const key = t.slice(0, eq).trim();
      if (key !== "ANDROID_HOME" && key !== "ANDROID_SDK_ROOT") continue;
      let val = t.slice(eq + 1).trim();
      if (
        (val.startsWith('"') && val.endsWith('"')) ||
        (val.startsWith("'") && val.endsWith("'"))
      ) {
        val = val.slice(1, -1);
      }
      if (val && !process.env[key]) process.env[key] = val;
    }
  } catch {
    /* no .env.local */
  }
}

function findSdkDir() {
  const fromEnv = [process.env.ANDROID_HOME, process.env.ANDROID_SDK_ROOT].filter(Boolean);
  for (const p of fromEnv) {
    const abs = path.resolve(p);
    if (existsSync(path.join(abs, "platform-tools"))) return abs;
    if (existsSync(abs)) return abs;
  }

  if (process.platform === "win32") {
    const local = process.env.LOCALAPPDATA;
    if (local) {
      const p = path.join(local, "Android", "Sdk");
      if (existsSync(path.join(p, "platform-tools"))) return p;
    }
    const pf86 = process.env["ProgramFiles(x86)"];
    if (pf86) {
      const p = path.join(pf86, "Android", "android-sdk");
      if (existsSync(path.join(p, "platform-tools"))) return p;
    }
  }

  if (process.platform === "darwin") {
    const p = path.join(os.homedir(), "Library", "Android", "sdk");
    if (existsSync(path.join(p, "platform-tools"))) return p;
  }

  const linuxDefault = path.join(os.homedir(), "Android", "Sdk");
  if (existsSync(path.join(linuxDefault, "platform-tools"))) return linuxDefault;

  return null;
}

export async function ensureAndroidSdk() {
  await mergeEnvLocal();

  if (existsSync(localProps)) {
    const txt = await readFile(localProps, "utf8");
    if (/^\s*sdk\.dir\s*=/m.test(txt)) {
      return;
    }
  }

  const sdk = findSdkDir();
  if (!sdk) {
    console.error(`
[habit] Android SDK not found.

Set ANDROID_HOME to your SDK path (Android Studio → Settings → Android SDK → "Android SDK location"), e.g.:
  Windows cmd:  set ANDROID_HOME=%LOCALAPPDATA%\\Android\\Sdk
  Git Bash:     export ANDROID_HOME="/c/Users/YOU/AppData/Local/Android/Sdk"

Or add to .env.local:
  ANDROID_HOME=C:/Users/YOU/AppData/Local/Android/Sdk

Or create ${localProps} with one line:
  sdk.dir=C:/Users/YOU/AppData/Local/Android/Sdk

Then run: pnpm cap:android
`);
    process.exit(1);
  }

  const sdkDirProp = sdk.replace(/\\/g, "/");
  const content = `# Generated by scripts/ensure-android-sdk.mjs — safe to edit\nsdk.dir=${sdkDirProp}\n`;
  await writeFile(localProps, content, "utf8");
  console.log("[ensure-android-sdk] wrote", localProps, "→", sdkDirProp);
}

const isMain =
  path.normalize(fileURLToPath(import.meta.url)) === path.normalize(process.argv[1] ?? "");
if (isMain) {
  ensureAndroidSdk().catch((e) => {
    console.error(e);
    process.exit(1);
  });
}
